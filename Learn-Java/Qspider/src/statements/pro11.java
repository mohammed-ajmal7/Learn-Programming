class pro11 {
    public static void main(String[] args) {

        int choice = 5;

        switch (choice) {
            case 1:
                System.out.println("100");
                break;

            case 2:
                System.out.println("200");
                break;

            case 3:
                System.out.println("300");
                break;

            case 4:
                System.out.println("400");
                break;

            case 5:
                System.out.println("500");
                break;

            case 6:
                System.out.println("600");
                break;

            default:
                System.out.println("Invalid Input");
        }
    }
}