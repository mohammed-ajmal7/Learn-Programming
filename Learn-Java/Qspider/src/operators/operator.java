package operators;

public class operator {
    public static void main(String[] args) {
        System.out.println(10 + 2 * 4 + 3);
        System.out.println(10 * 2 + 4 * 3);
        System.out.println(10 * 2 / 4 + 3);
        System.out.println(10 / 2 * 4 + 3);
        System.out.println(10 % 2 * 4 - 3);
        System.out.println(10 / 2 + 4 * 3);
        System.out.println(10 * 2 - 4 % 3);
    }
}
